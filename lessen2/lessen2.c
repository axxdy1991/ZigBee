#include<iocc2530.h>
void main()
{
  P0SEL &= 0xEF;//1110 1111将P0_4设置成普通IO
  P0DIR |= 0x10;//0001 0000将P0_4设置成输出IO
  
  P0SEL &= 0xBF;//1011 1111将P0_6设置成普通IO
  P0DIR &= 0xBF;//1011 1111将P0_6设置成输入IO
  P0INP &= 0xBF;//1011 1111将P0_6设置成上下拉模式
  P2INP &= 0xDF; //1101 1111将P0组处于上拉模式
  while(1)
  {
    if(1 == P0_6)
    {//检测到P0_6是高电平
      P0_4 = 0;
    }
    else
    {//检测到P0_6是低电平
      P0_4 = 1;
    }
  }
  
  
}